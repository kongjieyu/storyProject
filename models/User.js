const mongoose = require('mongoose');

//equivalent to: const {Schema} = mongoose;
const Schema = mongoose.Schema;

const UserSchema = new Schema({
    name: {
        type: String,
        required: true
    },
    email: {
        type: String,
        required: true
    },
    password: {
        type: String,
        required: true
    }
}, {timestamps: true});

mongoose.model('users',UserSchema);